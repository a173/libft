#include "libft.h"

int	ft_atoi(const char *a)
{
	int i;
	int net;
	int num;

	i = 0;
	net = 0;
	num = 0;
	while (a[i] && ((a[i] > 8 && a[i] < 14) || a[i] == 32))
		i++;
	if (a[i] == '-')
	{
		net++;
		i++;
	}
	else if (a[i] == '+')
		i++;
	while (a[i] && (a[i] > 47 && a[i] < 58))
	{
		num = num * 10 + a[i] - '0';
		i++;
	}
	if (net > 0)
		num = -num;
	return (num);
}
